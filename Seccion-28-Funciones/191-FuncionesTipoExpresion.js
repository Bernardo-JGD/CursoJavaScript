//podemos definir una variable y asignarle el valor de retorno de una funcion definida despues del signo de igual, pero debe de terminar con ";"
//a esto se le conoce como declaración de funcion de tipo Expresion
let sumar = function(a, b){return a + b};
//no tiene nombre esta funcion

//podemos utilizar la variable anterior, asignarle parámetros y nos devuelve el resultado de la funcion que le fue asignada
let resultado = sumar (1, 4);
console.log(resultado);
