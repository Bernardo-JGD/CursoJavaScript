/*Ya no se recomienda usar var*/
//Se puede declarar una variable sin poner var, let o const, pero no es una buena práctia
variable1 = "Gato";
console.log(variable1);

//Lo recomendable es colocar let antes del nombre de la variable
let variable2 = "Queso de gato";
console.log(variable2);

//Y se puede declarar la variable en una línea y asignar el valor en otra
let variable3;
variable3 = "Queso de Dragón";
console.log(variable3);

//las variables constantes se declaran con const, no son modificables

const constante = "Queso";
console.log(constante);
